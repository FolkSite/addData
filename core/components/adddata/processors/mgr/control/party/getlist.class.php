<?php

class addDataPartyGetListProcessor extends modObjectGetListProcessor
{
    public $classKey = 'addDataParty';
    public $defaultSortField = 'id';
    public $defaultSortDirection = 'ASC';


    /**
     * We do a special check of permissions
     * because our objects is not an instances of modAccessibleObject
     *
     * @return boolean|string
     */
    public function beforeQuery()
    {
        if (!$this->checkPermissions()) {
            return $this->modx->lexicon('access_denied');
        }

        return true;
    }


    /**
     * @param xPDOQuery $c
     *
     * @return xPDOQuery
     */
    public function prepareQueryBeforeCount(xPDOQuery $c)
    {
        $query = trim($this->getProperty('query'));
        if ($query) {
            $c->where(array(
                'party:LIKE' => "%{$query}%",
            ));
        }

        return $c;
    }


    /**
     * @param xPDOObject $object
     *
     * @return array
     */
    public function prepareRow(xPDOObject $object)
    {
        $array = $object->toArray();
        $array['actions'] = array();

        // Edit
        $array['actions'][] = array(
            'cls' => '',
            'icon' => 'icon icon-edit',
            'title' => $this->modx->lexicon('adddata_party_update'),
            'action' => 'updateParty',
            'button' => true,
            'menu' => true,
        );

        // Remove
        $array['actions'][] = array(
            'cls' => '',
            'icon' => 'icon icon-trash-o action-red',
            'title' => $this->modx->lexicon('adddata_party_remove'),
            'multiple' => $this->modx->lexicon('adddata_parties_remove'),
            'action' => 'removeParty',
            'button' => true,
            'menu' => true,
        );

        return $array;
    }

}

return 'addDataPartyGetListProcessor';